// Orientacao a objetos, assim como programacao procedural, é um tipo imperativo
// DB e programacao funcional, onde vc apenas declara e as coisas acontecem, ja em orientacao a objetos, é necessario declarar onde, quando as coisas acontecem

// Os programas sao 'objetos' que possuem uma série de propriedades.
// Pilares:
    //  - Herança
    //  - Polimorfismo
    //  - Encapsulamento
    //  - Abstracao


    // - Herança
        // O objeto filho herda propriedades e metodos do pai.
        // Porém o objeto filho também tem suas proprias caracteristicas.

    // - Polimorfismo
        // Objetos podem herdar a mesma classe pai, mas se comportarem de forma diferente quando invocamos seus metodos.

    // - Encapsulamento
        // Cada classe tem propriedades e métodos independentes do restante do codigo (escopo)

    // - Abstracao
        // Processo mental que consiste em isolar um aspecto determinado de um estado de coisas relativamente complexo, a fim de simplificar sua avaliacao, classificacao ou para permitir a comunicacao do mesmo.
        // Transformar um problema complexo, em algo simples, quebrar o problema em diversas partes.